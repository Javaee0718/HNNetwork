package com.womow.henan.modules.sys.user.bean.po;

import com.womow.henan.commons.bean.BaseEntity;

/**
 * @Description 用户实体类
 * @author CAI modules@163.com
 * @data 2017年8月7日下午5:53:08
 * @version V1.0
 */
public class BusUserEntity extends BaseEntity{

	private String username; //用户名
	private String nickname; //昵称
	private String password; //密码
	private String dept; //部门名称
	private Integer isDel; //是否删除
	
	
	public String getNickname() {
		return nickname;
	}
	public void setNickname(String nickname) {
		this.nickname = nickname;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getDept() {
		return dept;
	}
	public void setDept(String dept) {
		this.dept = dept;
	}
	public Integer getIsDel() {
		return isDel;
	}
	public void setIsDel(Integer isDel) {
		this.isDel = isDel;
	}
}
